<template>
    <form @submit.prevent="onsubmit">
        <label for="new-todo-input"> What's needs to be done? </label>
        <input 
        type="text"
        name="new-todo" 
        id="new-todo-input"
        autocomplete="off"
        v-model.lazy.trim="label" />
        <button type="submit">Add</button>
    </form>
</template>

<script>
export default {
    methods: {
        onsubmit() {
            if (this.label === "") {
                return;
            }
            this.$emit("todo-added", this.label);
            this.label = "";
        },
    },
    data() {
        return {
            label: "",
        };
    },
}
</script>